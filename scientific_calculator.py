import math


def sin_method(degree):
    return math.sin(math.radians(degree))

def cos_method(degree):
    return math.cos(math.radians(degree))


def tan_method(degree):
    return math.tan(math.radians(degree))

def sqrt_method(num):
    return math.sqrt(num)

def log_method(num):
    return math.log(num)

def exp_method(num):
    return math.exp(num)


